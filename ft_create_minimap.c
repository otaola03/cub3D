void	ft_create_minimap(void *mlx, void *mlx_win, int width, int height)
{
	int		x;
	int		y;
	t_img	*wall;
	t_img	*floor;
	int		size;

	size = 20;
	wall = ft_create_img(mlx, size, size);
	floor = ft_create_img(mlx, size, size);
	ft_edit_img(floor, size, size, 0xF300FF);
	ft_edit_img(wall, size, size, 0xC5FF00);

	y = -1;
	while (++y * size < mapHeight * size)
	{
		x = -1;
		while (++x * size < mapWidth * size)
		{
			if (worldMap[y][x] == 0)
				mlx_put_image_to_window(mlx, mlx_win, wall->mlx_img, x * size, y * size);
			else
				mlx_put_image_to_window(mlx, mlx_win, floor->mlx_img, x * size, y * size);
		}
	}
}
